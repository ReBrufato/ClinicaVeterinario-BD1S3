CREATE DATABASE VETERINARIA;

USE VETERINARIA;

CREATE TABLE VETERINARIO(
	ID	INTEGER, #PK
    CPF VARCHAR(15) NOT NULL UNIQUE,
    NOME VARCHAR(50) NOT NULL,
    DATA_NASC	DATE DEFAULT '0000-00-00',
    SALARIO	NUMERIC(7,2) NOT NULL,
    TELEFONE	VARCHAR(25) UNIQUE NOT NULL,
    CIDADE VARCHAR(50) NOT NULL,
    BAIRRO VARCHAR(50) NOT NULL,
    LOGRADOURO VARCHAR(50) NOT NULL,
    NUMERO	INTEGER NOT NULL,
    
    CONSTRAINT VETERINARIO_ID_PK PRIMARY KEY(ID),
    CONSTRAINT TELEFONE_UQ UNIQUE (TELEFONE)
);

CREATE TABLE MEDICAMENTO(
	RASTREABILIDADE INTEGER, #PK
    FABRICANTE VARCHAR(30) NOT NULL,
    NOME VARCHAR(30) NOT NULL,
    DATA_VALIDADE	DATE NOT NULL DEFAULT '0000-00-00',
    PRECO NUMERIC(7,2) NOT NULL,
    
    CONSTRAINT MEDICAMENTO_RASTREABILIDADE_PK PRIMARY KEY(RASTREABILIDADE)
);

CREATE TABLE DONO(
	ID INTEGER, #PK
    RG VARCHAR(15) NOT NULL UNIQUE,
    NOME VARCHAR(30) NOT NULL,
    DATA_NASC DATE DEFAULT '0000-00-00',
    TELEFONE VARCHAR(25) NOT NULL UNIQUE,
    CIDADE VARCHAR(50) NOT NULL,
    BAIRRO VARCHAR(50) NOT NULL,
    LOGRADOURO VARCHAR(50) NOT NULL,
    NUMERO	INTEGER NOT NULL,
    
    CONSTRAINT DONO_ID_PK PRIMARY KEY (ID)
);

CREATE TABLE ANIMAL(
	ID	 INTEGER, #PK
    ID_DONO INTEGER NOT NULL, #FK
    NOME	VARCHAR(30),
    DATA_NASC	DATE DEFAULT '0000-00-00',
    SEXO	VARCHAR(1) NOT NULL, #CK
    PORTE	VARCHAR(1), #CK
    AGRESSIVIDADE	VARCHAR(1) NOT NULL, #CK
    
    CONSTRAINT ANIMAL_ID_PK PRIMARY KEY(ID),
    CONSTRAINT ANIMAL_ID_DONO_FK FOREIGN KEY (ID_DONO) REFERENCES DONO(ID) ON DELETE CASCADE,
    CONSTRAINT ANIMAL_SEXO_CK CHECK (SEXO IN('M','F','m','f')),
    CONSTRAINT ANIMAL_PORTE_CK CHECK (PORTE IN('G','M','P','g','m','p')),
    CONSTRAINT ANIMAL_AGRESSIVIDADE CHECK (AGRESSIVIDADE IN('S','N','s','n'))
);

CREATE TABLE RECEPCIONISTA(
	ID INTEGER, #PK
    RG VARCHAR(15) NOT NULL UNIQUE,
    NOME VARCHAR(30) NOT NULL,
    DATA_NASC DATE DEFAULT '0000-00-00',
    SALARIO NUMERIC(7,2) NOT NULL,
    TELEFONE VARCHAR(25) NOT NULL UNIQUE,
    CIDADE VARCHAR(50) ,
    BAIRRO VARCHAR(50) NOT NULL,
    LOGRADOURO VARCHAR(50) NOT NULL,
    NUMERO	INTEGER NOT NULL,
    
    CONSTRAINT RECEPCIONISTA_ID_PK PRIMARY KEY (ID)
);

CREATE TABLE CONSULTA(
    NUMERO INTEGER, #PK
    ID_VETERINARIO INTEGER, #FK
    ID_RECEPCIONISTA INTEGER, #FK
    ID_ANIMAL INTEGER, #FK
    HORARIO TIME NOT NULL,
    
    CONSTRAINT CONSULTA_NUMERO_PK PRIMARY KEY(NUMERO),
    CONSTRAINT CONSULTA_ID_VETERINARIO_FK FOREIGN KEY (ID_VETERINARIO) REFERENCES VETERINARIO(ID) ON DELETE CASCADE,
    CONSTRAINT CONSULTA_ID_RECEPCIONISTA FOREIGN KEY (ID_RECEPCIONISTA) REFERENCES RECEPCIONISTA(ID) ON DELETE CASCADE,
    CONSTRAINT CONSULTA_ID_ANIMAL_FK FOREIGN KEY (ID_ANIMAL) REFERENCES ANIMAL(ID) ON DELETE CASCADE
);

CREATE TABLE ESPECIALIZACAO(
	ESPECIALIZACAO VARCHAR(30), #PK
    ID_VETERINARIO INTEGER NOT NULL, #FK
    
    CONSTRAINT ESPECIALIZACAO_ID_PK PRIMARY KEY (ESPECIALIZACAO),
    CONSTRAINT ESPECIALIZACAO_ID_VETERINARIO_FK FOREIGN KEY (ID_VETERINARIO) REFERENCES VETERINARIO(ID) ON DELETE CASCADE
);

CREATE TABLE CLASSE(
	CLASSE VARCHAR(30), #PK
    RASTR_MEDICAMENTO INTEGER NOT NULL, #FK
    
	CONSTRAINT CLASSE_CLASSE_PK PRIMARY KEY (CLASSE),
    CONSTRAINT CLASSE_RASTR_MEDICAMENTO_FK FOREIGN KEY (RASTR_MEDICAMENTO) REFERENCES MEDICAMENTO(RASTREABILIDADE) ON DELETE CASCADE
);

CREATE TABLE DOENCA_CRONICA(
	DOENCA	VARCHAR(30), #PK
    ID_ANIMAL INTEGER NOT NULL, #FK
    
    CONSTRAINT DOENCA_CRONCIA_DOENCA_PK PRIMARY KEY (DOENCA),
    CONSTRAINT DOENCA_CRONCIA_ID_ANIMAL_FK FOREIGN KEY (ID_ANIMAL) REFERENCES ANIMAL(ID) ON DELETE CASCADE
);

CREATE TABLE MEDICAMENTO_USADO(
	MEDIC_USADO VARCHAR(30), #PK
    ID_ANIMAL INTEGER NOT NULL, #FK
    
    CONSTRAINT MEDICAMENTO_USADO_MEDIC_USADO_PK PRIMARY KEY (MEDIC_USADO),
    CONSTRAINT MEDICAMENTO_USADO_ID_ANIMAL_FK FOREIGN KEY (ID_ANIMAL) REFERENCES ANIMAL(ID) ON DELETE CASCADE
);

CREATE TABLE UTILIZADO(
	RASTR_MEDICAMENTO INTEGER NOT NULL, #FK
    NUMERO_CONSULTA INTEGER NOT NULL, #FK
    DOSAGEM VARCHAR(50) NOT NULL,
    
    CONSTRAINT UTILIZADO_RASTR_MEDICAMENTO_FK FOREIGN KEY (RASTR_MEDICAMENTO) REFERENCES MEDICAMENTO(RASTREABILIDADE) ON DELETE CASCADE,
    CONSTRAINT UTILIZADO_NUMERO_CONSUTLA_FK FOREIGN KEY (NUMERO_CONSULTA) REFERENCES CONSULTA(NUMERO) ON DELETE CASCADE
);

CREATE TABLE IMPORTADO(
	RASTR_MEDICAMENTO INTEGER NOT NULL, #FK PK
    TRANSPORTE VARCHAR(50),
    PAIS VARCHAR(50),
    
    CONSTRAINT IMPORTADO_RASTR_MEDICAMENTO_PF PRIMARY KEY (RASTR_MEDICAMENTO),
    CONSTRAINT IMPORTADO_RASTR_MEDICAMENTO_FK FOREIGN KEY (RASTR_MEDICAMENTO) REFERENCES MEDICAMENTO(RASTREABILIDADE)
);

CREATE TABLE NACIONAL(
	RASTR_MEDICAMENTO INTEGER NOT NULL, #FK E PK
    CNPJ VARCHAR(50),
    
    CONSTRAINT NACIONAL_RASTR_MEDICAMENTO_PF PRIMARY KEY (RASTR_MEDICAMENTO),
    CONSTRAINT NACIONAL_RASTR_MEDICAMENTO_FK FOREIGN KEY (RASTR_MEDICAMENTO) REFERENCES MEDICAMENTO(RASTREABILIDADE)
);

