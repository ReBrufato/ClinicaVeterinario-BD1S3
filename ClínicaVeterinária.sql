	FROM CONSULTA C INNER JOIN VETERINARIO V ON C.ID_VETERINARIO = V.ID
    INNER JOIN UTILIZADO U ON U.NUMERO_CONSULTA = C.NUMERO
    INNER JOIN MEDICAMENTO M ON U.RASTR_MEDICAMENTO = M.RASTREABILIDADE;

