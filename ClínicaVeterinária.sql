SELECT C.NUMERO AS NUMERO_CONSULTA, C.HORARIO AS HORARIO_CONSULTA, R.NOME AS NOME_RECEPCIONISTA, R.CIDADE AS CIDADE_RECEPCIONISTA
	FROM CONSULTA C RIGHT OUTER JOIN RECEPCIONISTA R ON C.ID_RECEPCIONISTA = R.ID;

#FORMA UM GRUPO NA TABELA MEDICAMENTO DE ACORDO COM O NOME E DATA DE VALIDADE, EXIBINDO O NOME E DATA DE VALIDADE DE CADA PRODUTO, ALÉM
#DA SOMA DO PREÇO DE CADA GRUPO
SELECT NOME, DATA_VALIDADE, SUM(PRECO) AS SOMA_PREÇO
	FROM MEDICAMENTO
    GROUP BY NOME, DATA_VALIDADE
    ORDER BY NOME, DATA_VALIDADE;

#FAZ UM GRUPO NA TABELA ANIAML DE CADA PORTE COM CADA SEXO, E MOSTRA O PORTE, SEXO E NÚMERO DE ELEMENTOS DE CADA GRUPO
SELECT PORTE, SEXO, COUNT(*) AS NUMERO_ANIMAIS
	FROM ANIMAL
    GROUP BY PORTE,SEXO
    ORDER BY PORTE, SEXO;

#EXIBE O TRANSPORTE, PAÍS E QUANTIDADE DE PRODUTOS IMPORTANDOS FORMADOS POR UM AGRUPAMENTO DE TRANSPORTE E PAÍS, PORÉM APENAS PARA  
#OS AGRUPAMENTOS COM MAIS DE UM ELEMENTO
SELECT TRANSPORTE, PAIS, COUNT(*) AS QTD_IMPORTADO
	FROM IMPORTADO
    GROUP BY TRANSPORTE, PAIS
    HAVING COUNT(*) > 1;
    
#EXIBE O FRABRICANTE, NOME E MÉDIA DE PRECO DO CGRUPAMENTO FORMADO POR FABRICANTE E NOME, APENAS DOS GRUPOS QUE TIVEREM 
#O SOMATÓRIO DE PREÇOS MAIOR QUE 100
SELECT FABRICANTE, NOME, AVG(PRECO) AS PRECO_MEDICAMENTO
	FROM MEDICAMENTO
    GROUP BY FABRICANTE, NOME
    HAVING SUM(PRECO) > 100
    ORDER BY FABRICANTE, NOME;
 
